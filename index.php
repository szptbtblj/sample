<h3><概要></h3>
<h4>
位置情報型ARを用いて, 調布市観光マップで紹介されている観光名所をARで表示したAndroidアプリです. <br>ご利用の際は以下のインストールの手順と注意事項をお読みください.
</h4>
<h3><インストールの手順></h3>
<h4>
①以下のURLをクリックして利用者登録を行います.<br>
<a href="https://arlocation.herokuapp.com/regist.html">利用者登録</a><br>
②登録が確認された後, 登録されたメールアドレスにメールが送信されます.<br>
③表示されるURLをクリックしてGoogle Driveにアクセスします.<br>
④Drive上のapkファイルをインストールすることで, アプリケーションをインストールします.<br><br>
ご利用後はこちらのアンケートへのご協力をお願いします.<br>
<a href="b.html">アンケートへ</a>
</h4>
<br>
<h3><注意事項></h3>
<h4>
・インストールの手順④でご自身の端末の「提供元不明のアプリ」許可設定を行う必要があります. インストール後は必ず無効にしてください.<br>
・対応端末はAndroid5.0以降のものとなります.<br>
・ご利用の際, Googleアカウントが必要になります.
</h4>
